package Practicum;

public class EvenOrOddNumber {
    public static void main(String[] args) {

        int n = 3;
        // чётное - это то число которое делится на 2 без остатка
        // % - деление с остатком
        if (n % 2 == 0) {
            System.out.println("The number is even");
        } else {
            System.out.println("The number is odd");
        }
        int l = 11;
        // чётное - это то число которое делится на 2 без остатка
        // % - деление с остатком
        if (l % 3 == 0) {
            System.out.println("The number is even");
        } else {
            System.out.println("The number is odd");

            int q = 30;
            // чётное - это то число которое делится на 2 без остатка
            // % - деление с остатком
            if (q % 5 == 0) {
                System.out.println("The number is even");
            } else {
                System.out.println("The number is odd");

            }

        }
    }
}